let palabra = "animal";

switch (palabra) {
    case "perro":
        console.log("dog");
        break;
    case "gato":
        console.log("cat");
        break;
    case "puerta":
        console.log("door");
        break;
    case "mesa": 
        console.log("table");
    case "ventana":
        console.log("window");
        break;
    default:
        console.log("La palabra ingresada no es correcta");
        break;
}
